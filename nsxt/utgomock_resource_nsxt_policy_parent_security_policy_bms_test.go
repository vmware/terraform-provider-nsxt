func TestMockResourceNsxtPolicyParentSecurityPolicyBMSScopeValidation(t *testing.T) {
	testCases := []struct {
		name        string
		scope       []string
		expectValid bool
		groupTypes  map[string]int
	}{
		{
			name: "BMS only scope",
			scope: []string{
				"/infra/domains/default/groups/bms-production",
				"/infra/domains/default/groups/bms-interfaces",
			},
			expectValid: true,
			groupTypes: map[string]int{
				"bms": 2,
				"vm":  0,
			},
		},
		{
			name: "mixed BMS and VM scope",
			scope: []string{
				"/infra/domains/default/groups/bms-production",
				"/infra/domains/default/groups/vm-production",
				"/infra/domains/default/groups/bms-interfaces",
			},
			expectValid: true,
			groupTypes: map[string]int{
				"bms": 2,
				"vm":  1,
			},
		},
		{
			name: "VM only scope",
			scope: []string{
				"/infra/domains/default/groups/vm-production",
				"/infra/domains/default/groups/vm-development",
			},
			expectValid: true,
			groupTypes: map[string]int{
				"bms": 0,
				"vm":  2,
			},
		},
		{
			name:        "empty scope",
			scope:       []string{},
			expectValid: true, // Empty scope is valid (means "Any")
			groupTypes: map[string]int{
				"bms": 0,
				"vm":  0,
			},
		},
		{
			name: "invalid group path",
			scope: []string{
				"invalid-group-path",
			},
			expectValid: false,
			groupTypes: map[string]int{
				"unknown": 1,
			},
		},
	}

	for _, tc := range testCases {
		t.Run(tc.name, func(t *testing.T) {
			isValid := validateParentPolicyScope(tc.scope)
			groupAnalysis := analyzeParentPolicyScopeGroups(tc.scope)

			assert.Equal(t, tc.expectValid, isValid)

			for groupType, expectedCount := range tc.groupTypes {
				actualCount, exists := groupAnalysis[groupType]
				if expectedCount > 0 {
					assert.True(t, exists, "Group type %s should exist", groupType)
					assert.Equal(t, expectedCount, actualCount, "Count for group type %s", groupType)
				}
			}
		})
	}
}

