// Helper functions to simulate actual implementation logic
func validateParentPolicyScope(scope []string) bool {
	for _, groupPath := range scope {
		if !validateGroupPath(groupPath) {
			return false
		}
	}
	return true
}

func analyzeParentPolicyScopeGroups(scope []string) map[string]int {
	result := make(map[string]int)

	for _, groupPath := range scope {
		groupType := detectGroupType(groupPath)
		if count, exists := result[groupType]; exists {
			result[groupType] = count + 1
		} else {
			result[groupType] = 1
		}
	}

	return result
}

func createParentSecurityPolicyModel(displayName, description, category string, scope []string, stateful bool, sequenceNumber int64) model.SecurityPolicy {
	objType := "SecurityPolicy"

	policy := model.SecurityPolicy{
		DisplayName:    &displayName,
		Description:    &description,
		Scope:          scope,
		Stateful:       &stateful,
		SequenceNumber: &sequenceNumber,
		ResourceType:   &objType,
	}

	if category != "" {
		policy.Category = &category
	}

	return policy
}

func validateParentPolicyCategory(category string) bool {
	if category == "" {
		return true // Empty category is allowed
	}

	validCategories := []string{
		"Application",
		"Infrastructure",
		"Environment",
		"Emergency",
	}

	for _, valid := range validCategories {
		if category == valid {
			return true
		}
	}

	return false
}

func analyzeScopeGroupComposition(scope []string) map[string]interface{} {
	result := make(map[string]interface{})

	bmsCount := 0
	vmCount := 0
	interfaceCount := 0
	serverCount := 0

	for _, groupPath := range scope {
		groupType := detectGroupType(groupPath)

		switch groupType {
		case "bms":
			bmsCount++
			if contains(groupPath, "interface") {
				interfaceCount++
			} else {
				serverCount++
			}
		case "vm":
			vmCount++
			serverCount++
		}
	}

	result["bms_count"] = bmsCount
	result["vm_count"] = vmCount
	result["interface_count"] = interfaceCount
	result["server_count"] = serverCount
	result["mixed_scope"] = bmsCount > 0 && vmCount > 0

	return result
}
